package com.avit.itdap.bean.live;

public class LiveTreatBean {
	private String areaCode;
	private long serviceID;
	private long duration;
	private long userCount;
	private float audienceRate;
	private float MarketRate;
	private String channelName;
	private String areaName;	
	public String getAreaCode() {
		return areaCode;
	}
	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}
	public long getServiceID() {
		return serviceID;
	}
	public void setServiceID(long serviceID) {
		this.serviceID = serviceID;
	}
	public long getDuration() {
		return duration;
	}
	public void setDuration(long duration) {
		this.duration = duration;
	}
	public long getUserCount() {
		return userCount;
	}
	public void setUserCount(long userCount) {
		this.userCount = userCount;
	}
	public float getAudienceRate() {
		return audienceRate;
	}
	public void setAudienceRate(float audienceRate) {
		this.audienceRate = audienceRate;
	}
	public float getMarketRate() {
		return MarketRate;
	}
	public void setMarketRate(float marketRate) {
		MarketRate = marketRate;
	}
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	public String getAreaName() {
		return areaName;
	}
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	
	
	
}
