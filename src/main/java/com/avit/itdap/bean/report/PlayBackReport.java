package com.avit.itdap.bean.report;

public class PlayBackReport {
	
	private String epgId;
	
	private String epgName;
	
	private Integer timeCount;
	
	private Integer userCount;
	
	private Long sumDuration;
	
	private String serviceName;
	
	private Long avgDuration;

	public String getEpgId() {
		return epgId;
	}

	public void setEpgId(String epgId) {
		this.epgId = epgId;
	}

	public String getEpgName() {
		return epgName;
	}

	public void setEpgName(String epgName) {
		this.epgName = epgName;
	}

	public Integer getTimeCount() {
		return timeCount;
	}

	public void setTimeCount(Integer timeCount) {
		this.timeCount = timeCount;
	}

	public Integer getUserCount() {
		return userCount;
	}

	public void setUserCount(Integer userCount) {
		this.userCount = userCount;
	}

	public Long getSumDuration() {
		return sumDuration;
	}

	public void setSumDuration(Long sumDuration) {
		this.sumDuration = sumDuration;
	}

	public String getServiceName() {
		return serviceName;
	}

	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}

	
	public Long getAvgDuration() {
		return avgDuration;
	}

	public void setAvgDuration(Long avgDuration) {
		this.avgDuration = avgDuration;
	}

	@Override
	public String toString() {
		return "PlayBackReport [epgId=" + epgId + ", epgName=" + epgName + ", timeCount=" + timeCount + ", userCount="
				+ userCount + ", sumDuration=" + sumDuration + ", serviceName=" + serviceName + "]";
	}
	

	
}
